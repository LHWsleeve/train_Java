package TestJavaassist;

public class Emp {
    private int empo;
    private String name;

    public Emp(int empo, String name) {
        this.empo = empo;
        this.name = name;
    }

    public Emp() {
    }

    public int getEmpo() {
        return empo;
    }

    public void setEmpo(int empo) {
        this.empo = empo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
